//control flow
//estructura del ciclo for lo que hace es evaluar a verdadero o falso la condiciones que tenga
for(let i = 0; i < 5; i++){
   //registro en la consola
   console.log('in loop:', i);//cada vez que pasa por el ciclo se agregara el contador uno a uno
}
//ciclo terminado se ejecuta cuando pasa las veces que se previas que asignamos
console.log('Loop finished');